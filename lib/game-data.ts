function normalizeLanguage(language: string): SupportedLanguage {
	return language === "es" ? "es" : "en";
}

const CATEGORY_ID_SET = new Set<string>(GAME_CATEGORIES.map((category) => category.id));

export function isCategorySelection(value: unknown): value is GameCategorySelection {
	if (value === ALL_CATEGORIES_ID) return true;
	if (Array.isArray(value)) {
		return value.length > 0 && value.every((item) => typeof item === "string" && CATEGORY_ID_SET.has(item));
	}
	return false;
}

export function migrateCategorySelection(value: unknown): GameCategorySelection {
	if (value === ALL_CATEGORIES_ID) return ALL_CATEGORIES_ID;
	if (Array.isArray(value) && isCategorySelection(value)) return value;
	if (typeof value === "string" && CATEGORY_ID_SET.has(value)) {
		return [value as GameCategoryId];
	}
	return DEFAULT_CATEGORY_SELECTION;
}

export const TOTAL_WORD_COUNT = GAME_CATEGORIES.reduce((sum, category) => sum + category.words.length, 0);

export function getSelectionWordCount(selection: GameCategorySelection): number {
	if (selection === ALL_CATEGORIES_ID) return TOTAL_WORD_COUNT;
	return GAME_CATEGORIES
		.filter((category) => selection.includes(category.id))
		.reduce((sum, category) => sum + category.words.length, 0);
}

export function getCategoryOptions(language: SupportedLanguage | string): CategoryOption[] {
	const lang = normalizeLanguage(language);
	return GAME_CATEGORIES.map((category) => ({
		id: category.id,
		label: category.label[lang],
		wordCount: category.words.length,
	}));
}

export function getCategoryLabel(selection: GameCategorySelection, language: SupportedLanguage | string): string {
	const lang = normalizeLanguage(language);
	if (selection === ALL_CATEGORIES_ID) return ALL_CATEGORIES_LABEL[lang];
	if (selection.length === 1) {
		const category = GAME_CATEGORIES.find((item) => item.id === selection[0]);
		return category?.label[lang] ?? ALL_CATEGORIES_LABEL[lang];
	}
	return `${selection.length} ${lang === "es" ? "categorías" : "categories"}`;
}

export function pickRandomWord(
	language: SupportedLanguage | string,
	categorySelection: GameCategorySelection = DEFAULT_CATEGORY_SELECTION,
): RandomWordSelection {
	const lang = normalizeLanguage(language);
	const categories =
		categorySelection === ALL_CATEGORIES_ID
			? GAME_CATEGORIES
			: GAME_CATEGORIES.filter((category) => categorySelection.includes(category.id));

	const safeCategories = categories.length > 0 ? categories : GAME_CATEGORIES;
	const selectedCategory = safeCategories[Math.floor(Math.random() * safeCategories.length)] ?? GAME_CATEGORIES[0];
	const selectedWord = selectedCategory.words[Math.floor(Math.random() * selectedCategory.words.length)] ?? selectedCategory.words[0];

	return {
		categoryId: selectedCategory.id,
		categoryLabel: selectedCategory.label[lang],
		word: selectedWord.word[lang],
		hint: selectedWord.hint[lang],
	};
}
